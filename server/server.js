Meteor.startup(function () {
  // code to run on server at startup
  if (Attributes.find().count() === 0) {
    var names = ["Beer",
                 "Bug fixed",
                 "Email sent to client",
                 "Feature deployed",
                 "Meeting scheduled",
                 "Salaries payed"];
    for (var i = 0; i < names.length; i++)
      Attributes.insert({name: names[i], value: 0});
  }
});
